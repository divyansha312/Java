
public class IndexOf {

	public static void main(String[] args) {
     String s1 = "Divya is a good girl";
      System.out.println(s1.indexOf("d"));
      System.out.println(s1.lastIndexOf("i"));
      System.out.println(s1.charAt(7));
      System.out.println(s1.contains("y"));
      System.out.println(s1.startsWith("Di"));
      System.out.println(s1.endsWith("la"));
     
	}

}
