
public class StringPlusOperator {

	public static void main(String[] args) {
     String s1 = "Divya";
     String s2 = "Java";
     System.out.println(s1 + s2);
     System.out.println(s1+10+20);
     System.out.println(10+20+s1);
     System.out.println(10+s1+20);
     System.out.println(s1+20/10);
     System.out.println(s1+20*2);
    // System.out.println(s1+5-5);  throws error 
	}

}
